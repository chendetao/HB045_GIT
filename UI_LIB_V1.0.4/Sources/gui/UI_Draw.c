/*
 * 画一条直线
 */
void draw_line(int x1, int y1, int x2, int y2, unsigned short color)
{
    int dx, dy, e;
    dx = x2 - x1;
    dy = y2 - y1;

    if (dx >= 0)
    {
        if (dy >= 0) // dy>=0
        {
            if (dx >= dy) // 1/8 octant
            {
                e = dy - (dx >> 1);
                while (x1 <= x2)
                {
                    setpixel(x1, y1, color);

                    if (e > 0)
                    {y1 += 1; e -= dx;}
                    x1 += 1;
                    e += dy;
                }
            }
            else        // 2/8 octant
            {
                e = dx - (dy >> 1);
                while (y1 <= y2)
                {
                    setpixel(x1, y1, color);
                    if (e > 0)
                    {x1 += 1; e -= dy;}
                    y1 += 1;
                    e += dx;
                }
            }
        }
        else           // dy<0
        {
            dy = -dy;   // dy=abs(dy)

            if (dx >= dy) // 8/8 octant
            {
                e = dy - (dx >> 1);
                while (x1 <= x2)
                {
                    setpixel(x1, y1, color);
                    
                    if (e > 0)
                    {y1 -= 1; e -= dx;}
                    x1 += 1;
                    e += dy;
                }
            }
            else        // 7/8 octant
            {
                e = dx - (dy >> 1);
                while (y1 >= y2)
                {
                    setpixel(x1, y1, color);
                    if (e > 0)
                    {x1 += 1; e -= dy;}
                    y1 -= 1;
                    e += dx;
                }
            }
        }
    }
    else //dx<0
    {
        dx = -dx;     //dx=abs(dx)
        if (dy >= 0) // dy>=0
        {
            if (dx >= dy) // 4/8 octant
            {
                e = dy - (dx >> 1);
                while (x1 >= x2)
                {
                    setpixel(x1, y1, color);
                    if (e > 0)
                    {y1 += 1; e -= dx;}
                    x1 -= 1;
                    e += dy;
                }
            }
            else        // 3/8 octant
            {
                e = dx - (dy >> 1);
                while (y1 <= y2)
                {
                    setpixel(x1, y1, color);
                    if (e > 0)
                    {x1 -= 1; e -= dy;}
                    y1 += 1;
                    e += dx;
                }
            }
        }
        else           // dy<0
        {
            dy = -dy;   // dy=abs(dy)

            if (dx >= dy) // 5/8 octant
            {
                e = dy - (dx >> 1);
                while (x1 >= x2)
                {
                    setpixel(x1, y1, color); 
                    if (e > 0)
                    {y1 -= 1; e -= dx;}
                    x1 -= 1;
                    e += dy;
                }
            }
            else        // 6/8 octant
            {
                e = dx - (dy >> 1);
                while (y1 >= y2)
                {
                    setpixel(x1, y1, color); 
                    if (e > 0)
                    {x1 -= 1; e -= dy;}
                    y1 -= 1;
                    e += dx;
                }
            }
        }
    }
}

////////////////////////////////////////////////////////////////////////////////
//                                                                            //
//  void DrawUI_Bitmap( int x, int y, int w, int h, \                         //
//	const unsigned char *bitmap,unsigned long F, unsigned long B)             //
//  x,y start coordinate                                                      //
//  w,h Bitmap width and height                                               //
//  bitmap Bitmap data pointer                                                //
//  该函数用于绘制单色位图, xy为起始坐标,wh为宽度和高度                       //
//  bitmap为图片数据指针,FB为图片数据中01对应的颜色.                          //
//  需要注意的是, 图片的高度必须设置为8的整数倍                               //
//                                                                            //
////////////////////////////////////////////////////////////////////////////////

void DrawUI_Bitmap( int x, int y, int w, int h, \
	const unsigned char *bitmap,unsigned long F, unsigned long B)
{   
	unsigned long temp;
	
	if ( h % 8 ){ // 错误的宽度
		return;
	}
	
	for ( int k = 0; k < w; k++ )
	for ( int i = 0; i < h/8; i++ )
	{
		temp = bitmap[(h/8)*k+i];
	
		for ( int j = 0; j < 8; j++ )
		{
			if ( (temp>>j) & 0x1)
			{
				setpixel(x+k, i*8+y+j, F);
			}
			else
			{
				setpixel(x+k, i*8+y+j, B);
			}
		}	
	}
}

