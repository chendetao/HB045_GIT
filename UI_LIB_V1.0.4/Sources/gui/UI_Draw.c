void UI_Draw_Font_10x24( int x, int y, int idx )
{	
	unsigned char temp;
	
    for ( int t = 0; t < 30; t += 3 )
    {
        temp = number_10x24[idx][t+0];

        for ( int t1 = 0; t1 < 8; t1++)
        {
            if (temp & 0x1)
            {
                setpixel( (x), (y+t1+0), 0xff);
            }

            temp >>= 1;
        }
           
        temp = number_10x24[idx][t+1];
        for ( int t1 = 0; t1 < 8; t1++)
        {
            if (temp & 0x1)
            {
                setpixel( (x), (y+t1+8), 0xff);
            }

            temp >>= 1;
        }
        
        temp = number_10x24[idx][t+2];
        for ( int t1 = 0; t1 < 8; t1++)
        {
            if (temp & 0x1)
            {
                setpixel( (x), (y+t1+16), 0xff);
            }

            temp >>= 1;
        }
		
		x++;
    }
}

void draw_bitmap(int fx, int fy, int width, int height, const unsigned char *bitmap )
{
	unsigned short rgb;
	const unsigned char *p = bitmap;
	
	for( int y = fy; y < (fy + height); y++ )
	{
		for (  int x = fx; x < ( fx + width); x++ )
		{
			if ( x < 80 && y < 160 )  {
				rgb = ((unsigned short)p[1]<<8) | p[0]; 
				p += 2;
				frame_buffer[y][x] = rgb;
			}
		}
	}
}

