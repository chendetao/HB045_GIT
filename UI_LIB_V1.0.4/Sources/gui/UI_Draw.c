void UI_Draw_Val( int x, int y, int value)
{
	int count;
	
	if ( value > 99999 ){ value = 99999; }
	
	if ( value > 9999 ){
		count = 5;
	}else if ( value > 999 ){
		count = 4;
	}else if ( value > 99 ){
		count = 3;
	}else if ( value > 9 ){
		count = 2;
	}else{
		count = 1;
	}
	
	int sx = x+((96-x) - count*8)/2;
	
	int ex = sx + (count-1) * 8;
	do
	{
		UI_Draw_Font_8x12(ex, y, value%10);
		value /= 10; ex -= 8;
	}while( value > 0 );	
}

